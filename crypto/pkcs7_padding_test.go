func TestPKCS7Pad(t *testing.T) {
	for i, c := range cases {
		got := PKCS7Pad(c.in)
		if bytes.Compare(c.out, got) != 0 {
			t.Errorf("%d: expected %x, got %x", i, c.out, got)
		}
	}
}

func TestPKCS7Unpad(t *testing.T) {
	for i, c := range cases {
		got := PKCS7Unpad(c.out)
		if bytes.Compare(c.in, got) != 0 {
			t.Errorf("%d: expected %x, got %x", i, c.in, got)
		}
	}
}
